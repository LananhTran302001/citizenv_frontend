<template>
  <b-container fluid class="container-style">
    <!-- Thông báo từ server -->
    <Message
      :id="serverMsg.id"
      :title="serverMsg.title"
      :content="serverMsg.content"
      :variant="serverMsg.variant"
    />
    <!-- User interface control -->
    <b-row>
      <!-- Search bar -->
      <b-col xs="10" sm="10" md="6" lg="6" class="my-1">
        <b-form-group
          label="Tìm kiếm"
          label-for="search-input"
          label-cols-xs="12"
          label-cols-sm="4"
          label-cols-md="3"
          label-cols-lg="2"
          label-align-sm="right"
          label-size="sm"
          class="mb-0"
        >
          <b-input-group size="sm">
            <!-- Chỗ nhập input -->
            <b-form-input
              id="search-input"
              v-model="filter"
              type="search"
              placeholder="Nhập để tìm kiếm"
            >
            </b-form-input>
            <!-- Nút Clear -->
            <b-input-group-append>
              <b-button>Clear</b-button>
            </b-input-group-append>
          </b-input-group>
        </b-form-group>
      </b-col>

      <!-- Rows per page -->
      <b-col xs="10" sm="10" md="6" lg="6" class="my-1">
        <b-form-group
          label="Số dòng/trang"
          label-cols-xs="12"
          label-cols-sm="4"
          label-cols-md="5"
          label-cols-lg="3"
          label-align-sm="right"
          label-size="sm"
          class="mb-0"
        >
          <b-form-select
            :options="rowsPerPageOptions"
            v-model="rowsPerPage"
            size="sm"
          ></b-form-select>
        </b-form-group>
      </b-col>
    </b-row>
    <!-- Selection -->
    <b-row>
      <b-col xs="12" sm="12" md="6" lg="3" class="my-1">
        <!-- Chọn tỉnh thành: A1: role = 1 -->
        <b-form-group
          v-if="user.role == 1"
          label="Tỉnh thành"
          label-cols-xs="12"
          label-cols-sm="4"
          label-cols-md="5"
          label-cols-lg="4"
          label-align-sm="right"
          label-size="sm"
          class="mb-0"
        >
          <b-form-select
            :options="citiesName"
            v-model="currentCity"
            size="sm"
          ></b-form-select>
        </b-form-group>
      </b-col>

      <b-col xs="12" sm="12" md="6" lg="3" class="my-1">
        <!-- Chọn quận/huyện/thị xã: A1, A2: role = 1,2 -->
        <b-form-group
          v-if="user.role > 0 && user.role < 3"
          label="Quận/huyện/thị xã"
          label-cols-xs="12"
          label-cols-sm="4"
          label-cols-md="5"
          label-cols-lg="5"
          label-align-sm="right"
          label-size="sm"
          class="mb-0"
        >
          <b-form-select
            :options="districtsName"
            v-model="currentDistrict"
            size="sm"
          ></b-form-select>
        </b-form-group>
      </b-col>

      <b-col xs="12" sm="12" md="6" lg="3" class="my-1">
        <!-- Chọn xã/phường/thị trấn: A1, A2, A3: role = 1,2,3 -->
        <b-form-group
          v-if="user.role > 0 && user.role < 4"
          label="Xã/phường/thị trấn"
          label-cols-xs="12"
          label-cols-sm="4"
          label-cols-md="5"
          label-cols-lg="6"
          label-align-sm="right"
          label-size="sm"
          class="mb-0"
        >
          <b-form-select
            :options="wardsName"
            v-model="currentWard"
            size="sm"
          ></b-form-select>
        </b-form-group>
      </b-col>

      <b-col xs="12" sm="12" md="6" lg="3" class="my-1">
        <!-- Chọn thôn/xóm/bản/tổ dân phố: A1, A2, A3, B1=A4: role = 1,2,3,4 -->
        <b-form-group
          v-if="user.role > 0 && user.role < 5"
          label="Thôn/bản/tổ dân phố"
          label-cols-xs="12"
          label-cols-sm="4"
          label-cols-md="5"
          label-cols-lg="5"
          label-align-sm="right"
          label-size="sm"
          class="mb-0"
        >
          <b-form-select
            :options="residentalGroupsName"
            v-model="currentResidentalGroup"
            size="sm"
          ></b-form-select>
        </b-form-group>
      </b-col>
    </b-row>

    <SelectForm :options="cities"/>

    <!-- Main table -->
    <b-table
      :items="items"
      :fields="fields"
      :current-page="currentPage"
      :per-page="rowsPerPage"
      :filter="filter"
      sort-icon-left
      responsive="sm"
      class="my-2 table-style"
    >
      <template #cell(index)="row">
        {{ row.index + 1 }}
      </template>
      <template #cell(authorization)="data">
        <b-button
          size="xs"
          class="mr-2 sm-button-style sm-button-style"
          v-if="user.role == 4 && !user.is_locked"
        >
          <font-awesome-icon
            icon="trash"
            size="sm"
            @click="deleteRow(data.name)"
          />
        </b-button>
        <b-button
          size="xs"
          class="mr-2 sm-button-style sm-button-style"
          v-if="user.role == 4 && !user.is_locked"
        >
          <font-awesome-icon
            icon="edit"
            size="sm"
            @click="editRow(data.name)"
          />
        </b-button>
        <b-button size="xs" class="mr-2 sm-button-style sm-button-style">
          <font-awesome-icon
            icon="eye"
            size="sm"
            @click="detailsRow(data.name)"
          />
        </b-button>
      </template>
    </b-table>
    <!-- Total rows -->
    <b-row class="my-2">
      <span class="ml-5">Danh sách có tổng cộng {{ totalRows }} dòng </span>
    </b-row>
    <!-- Pages of table -->
    <b-pagination
      v-model="currentPage"
      :total-rows="totalRows"
      :per-page="rowsPerPage"
      aria-controls="area-table"
      class="justify-content-center"
    ></b-pagination>

    <!-- Chọn các cột hiển thị -->
    <div class="select-cols-style">
      <div class="menu-style" v-show="isOpenSelectCols">
        <h5>Chọn cột hiển thị</h5>
        <CheckboxButton
          v-for="(field, i) in api.fields"
          :key="i"
          :label="field.label"
          @isChecked="selectColIsChecked(api.fields[i])"
          @isNonChecked="selectColIsNonChecked(api.fields[i])"
        />
        <b-row class="justify-content-start">
          <b-col cols="8">
            <b-button
              type="button"
              variant="primary"
              class="close-selcolumn-btn"
              :disabled="showFields.length == 0"
              @click="applySelectCols"
            >
              Áp dụng
            </b-button>
          </b-col>
          <b-col cols="4">
            <b-button
              type="button"
              variant="secondary"
              class="close-selcolumn-btn"
              @click="hideSelectCols"
            >
              Hủy
            </b-button>
          </b-col>
        </b-row>
      </div>
      <font-awesome-icon
        icon="table"
        size="lg"
        id="open-selcolumn-btn"
        v-show="!isOpenSelectCols"
        @click="openSelectCols"
      />
    </div>
  </b-container>
</template>

<script>
import CheckboxButton from "./buttons/CheckboxButton.vue";
import { mapGetters } from "vuex";
import { BACKEND_URL } from "../../store/statics/backend_url";
import Message from "../../components/Message.vue";
import SelectForm from "../../components/table/forms/SelectForm.vue"
import {
  getCitizenAPI,
  getCitiesAPI,
  getDistrictsAPI,
  getWardsAPI,
  getGroupsAPI,
} from "../../store/statics/citizen_constants";

export default {
  name: "CitizenTable",
  components: {
    Message,
    SelectForm,
    CheckboxButton,
  },

  data: function () {
    return {
      totalRows: 1, // Tổng số dòng
      currentPage: 1, // Trang bảng hiện tại
      rowsPerPageOptions: [5, 10, 30, 50, 100], // Các options cho số dòng hiện / trang bảng
      rowsPerPage: 10, // Số dòng/trang đang chọn
      filter: null, // Phần text tìm kiếm trong bảng

      // AREA FILTER
      // Các tỉnh/thành phố cho phép chọn: chỉ được xem trong option này
      cities: [],
      // Các quận/huyện/thị xã cho phép chọn: chỉ được xem trong option này
      districts: [],
      // Các xã/phường/thị trấn cho phép chọn: chỉ được xem trong option này
      wards: [],
      // Các thôn/xóm/tổ dân phố cho phép chọn: chỉ được xem trong option này
      residentalGroups: [],


      // AREA NAME
      // 
      citiesName: ["Ha Noi", "Ha Giang", "Can Tho"],
      // Các quận/huyện/thị xã cho phép chọn: chỉ được xem trong option này
      districtsName: ["Quận 1", "Quận 2", "Quận 3"],
      // Các xã/phường/thị trấn cho phép chọn: chỉ được xem trong option này
      wardsName: ["Xã 1", "Xã 12", "Xã 15"],
      // Các thôn/xóm/tổ dân phố cho phép chọn: chỉ được xem trong option này
      residentalGroupsName: ["Thôn 1", "Thôn 2", "Thôn 3"],


      // CHOICE FILTER
      currentCity: null,
      currentDistrict: null,
      currentWard: null,
      currentResidentalGroup: null,

      // DATA:
      api: null,
      items: [],
      fields: [],
      showFields: [],
      isOpenSelectCols: false,
      multiAreas: [],
    };
  },

  computed: {
    ...mapGetters({
      user: "User/getUser",
      serverMsg: "Citizen/getServerMsg"
    }),
  },

  created() {
    this.api = getCitizenAPI();
    console.log(this.fields);
    this.fields = this.api.fields.slice(0, this.api.fields.length);
    console.log(this.fields);
    this.fields.push({ key: "authorization", label: "Chỉnh sửa" });
    this.fields.unshift({ key: "index", label: "STT" });
    this.fetchData();
  },

  methods: {
    reset() {
      this.fields = null;
      this.isOpenSelectCols = false;
    },

    deleteRow(val) {
      console.log(val);
    },
    editRow(val) {
      console.log(val);
    },
    detailsRow(val) {
      console.log(val);
    },

    getAreaNames(areas) {
      let list = [];
      for (let area of areas) {
        list.push(area.Name);
      }
      return list;
    },

    fetchData(areaId) {
      this.fetchAreas();
      let url = BACKEND_URL + this.api.urlAll;
      if (areaId) {
        url = `${url}/${areaId}`;
      }
      console.log("----------url-----------")
      console.log(url)
      fetch(url, {
        headers: { Authorization: `Bearer ${localStorage.token}` },
      })
        .then((response) => {
          return response.json();
        })
        .then((data) => {
          // this accounts for api urls in which the data is not the first result
          this.items = data.Citizens;
          console.log(data);
          this.totalRows = this.items.length;
        });
    },

    fetchAreas() {
      switch (this.user.role) {
        case 1:
          this.fetchCities();
          break;
        case 2:
          this.fetchDistrictsInCity(null);
          break;
        case 3:
          this.fetchWardsInDistrict(null);
          break;
        case 4:
          this.fetchGroupsInWard(null);
          break;
        default:
          console.log("Làm j có quyền");
      }
    },

    fetchCities() {
      let url = BACKEND_URL + getCitiesAPI().url;
      url = `${url}/${this.user.user_id}`;
      fetch(
        url,
        {
          headers: { Authorization: `Bearer ${localStorage.token}` },
        }
      )
        .then((response) => {
          return response.json();
        })
        .then((data) => {
          // this accounts for api urls in which the data is not the first result
          this.cities = data.Areas;
          this.citiesName = this.getAreaNames(data.Areas);
          console.log("Đây là các tỉnh");
          console.log(data);
        });
    },

    fetchDistrictsInCity(cityId) {
      if (!cityId) {
        cityId = this.user.user_id;
      }
      let url = BACKEND_URL + getDistrictsAPI().url;
      url = `${url}/${cityId}`;
      fetch(url, {
        headers: { Authorization: `Bearer ${localStorage.token}` },
      })
        .then((response) => {
          return response.json();
        })
        .then((data) => {
          // this accounts for api urls in which the data is not the first result
          this.districts = data.Areas;
          this.districtsName = this.getAreaNames(data.Areas);
          console.log("Đây là các huyện");
          console.log(data);
        });
    },

    fetchWardsInDistrict(districtId) {
      if (!districtId) {
        districtId = this.user.user_id;
      }
      let url = BACKEND_URL + getWardsAPI().url;
      url = `${url}/${districtId}`;
      fetch(url, {
        headers: { Authorization: `Bearer ${localStorage.token}` },
      })
        .then((response) => {
          return response.json();
        })
        .then((data) => {
          // this accounts for api urls in which the data is not the first result
          this.wards = data.Areas;
          this.wardsName = this.getAreaNames(data.Areas);
          console.log("Đây là các xã");
          console.log(data);
        });
    },

    fetchGroupsInWard(wardId) {
      if (!wardId) {
        wardId = this.user.user_id;
      }
      let url = BACKEND_URL + getGroupsAPI().url;
      url = `${url}/${wardId}`;
      fetch(url, {
        headers: { Authorization: `Bearer ${localStorage.token}` },
      })
        .then((response) => {
          return response.json();
        })
        .then((data) => {
          // this accounts for api urls in which the data is not the first result
          this.residentalGroups = data.Areas;
          this.residentalGroupsName = this.getAreaNames(data.Areas);
          console.log("Đây là các thôn");
          console.log(data);
        });
    },

    openSelectCols() {
      this.isOpenSelectCols = true;
    },

    hideSelectCols() {
      this.isOpenSelectCols = false;
    },

    // Áp dụng danh sách các cột sẽ hiện vào bảng
    applySelectCols() {
      this.hideSelectCols();
      let list = Array.of(...this.api.fields);
      let listnew = [];
      this.reset();
      for (let field of list) {
        if (this.showFields.indexOf(field) != -1) {
          listnew.push(field);
        }
      }
      this.fields = listnew;
    },

    // Thêm 1 cột trong danh sách hiện
    selectColIsChecked(row) {
      this.showFields.push(row);
    },

    // Xóa 1 cột trong danh sách hiện
    selectColIsNonChecked(row) {
      this.showFields.splice(this.showFields.indexOf(row), 1);
    },

  },

  

  watch: {
    currentCity: function (val) {
      this.currentCity = val;
      if (val) {
        let city = this.cities.find(obj =>  obj.Name === val );
        console.log("Đây là city bạn chọn")
        console.log(city);
        this.fetchDistrictsInCity(city.Id)
        this.fetchData(city.Id)
      }
    },

    currentDistrict: function (val) {
      this.currentDistrict = val;
      if (val) {
        let district = this.districts.find(obj =>  obj.Name === val );
        console.log("Đây là district bạn chọn")
        console.log(district);
        this.fetchWardsInDistrict(district.Id)
        this.fetchData(district.Id)
      }
    },

    currentWard: function (val) {
      this.currentWard = val;
      if (val) {
        let ward = this.wards.find(obj =>  obj.Name === val );
        console.log("Đây là ward bạn chọn")
        console.log(ward);
        this.fetchGroupsInWard(ward.Id)
        this.fetchData(ward.Id)
      }
    },

    currentResidentalGroup: function (val) {
      this.currentResidentalGroup = val;
      if (val) {
        let group = this.residentalGroups.find(obj =>  obj.Name === val );
        console.log("Đây là group bạn chọn")
        console.log(group);
        this.fetchData(group.Id)
      }
    }
  }
};
</script>

